package Chapter15_inherit_figure;

public class Figure {
	private int height;
	private int width;

	public Figure(int height, int width) {
		super();
		this.height = height;
		this.width = width;
	}

	public void area() {
		
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}
	
}
